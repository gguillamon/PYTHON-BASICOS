#-9-Ejercicio que calcula la superficie de un triaungulo, pide por teclado base y altura
print("Vamos a calcular el area de un triangulo...")
base=int(input("Introduzca la base ( en cm): "))
altura=int(input("Introduzca la altura( en cm: "))
area=(base*altura)/2
print("El superficie del triangulo es de %.2f " % area)